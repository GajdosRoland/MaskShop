    <h1 class="display-4">A maszkok története</h1>
    <p>Identitásunkat a testünkre vetítjük ki, de elsősorban az arc az, amivel énünket azonosítjuk. Minden kultúrában a
        szociális identitás és státusz (házas/egyedülálló, szent/profán, vezető/közember) szimbolikus, és megköveteli a
        testnek és az arcnak az identitáshoz és annak változásaihoz való igazodását. Ez rendszerint valaminek az
        elvételével (pl. fogak, haj), vagy valaminek a hozzáadásával (jelmez, maszk, arcfestés) valósul meg. A maszk az
        arc teljes vagy részbeni eltakarásával rendkívüli szerepet játszhat az identitás megváltoztatásában.</p>
    <p>A személy (person) szó a görög maszk szóból származik, amely egyben a szerepet is jelentette, amit a drámában
        játszott a színész. A maszk segítségével elrejthetjük, vagy éppen átváltoztathatjuk a külső kép mögötti ént
        valaki, vagy valami mássá. Ennek a metaforikus másiknak, az "olyan mint" állapotnak a létrehozása fontos
        tulajdonsága a maszknak: mindkettőjüket - az eredeti és az átváltozott személyt is - hatalmassá és játékossá
        teszi, kapcsolatot teremtve a rituálékkal, a vallással vagy a mítosszal.</p>
    <p>A maszkok használata 20 ezer évre nyúlik vissza, maszkokat készítettek az ó- és újkori civilizációk egyaránt.
        Halotti maszkkal került az egyiptomi múmiát sírhelyére, és az segítette a visszatérő lelket saját teste
        felismerésében. Ázsiában, Észak-Amerikában és Afrikában a sámánok rituáléinak sok esetben elengedhetetlen
        kelléke volt. Használták a maják és az aztékok Közép-Amerikában, de az inkák és más Andok-beli civilizációk is.
        Kínában, Indiában és Japánban az ősidők óta sokféle módon használták a maszkokat, így például a görögökhöz és a rómaikhoz hasonlóan a színjátszásban.</p>
    <p>A görög dráma kezdetben egy maszkos rituálé volt, idővel azonban a vallásos aspektus háttérbe szorult a
        szekularizáció és a szórakoztatás javára. Indonéziában, Indiában, Kínában, Japánban és Európában a maszkos
        színjátszás napjainkig fennmaradt, többé-kevésbé vallásos jelleggel.</p>
    <p>A korai keresztény egyház a pogány rítusokkal hozta kapcsolatba és elítélte használatát. Viselését az
        erkölcstelen viselkedéssel hozták összefüggésbe, amit a maszk névtelensége biztosíthatott. Az egyház maszk
        elleni hadjárata azonban nem bizonyult sikeresnek, az fennmaradt a karneválokban, a reneszánsz idején a
        Commedia del' Arteban, később pedig a színjátszás segítségével egész Európában elterjed.</p>
    <p>A törzsi kultúrákban a maszkokat gyakran a betegségek gyógyítására, a boszorkányság és átok elűzésére, a
        szenvedés okainak megszűntetésére használták. A szellemvilágba való utazáshoz a sámánok szintén gyakran
        alkalmazták. A maszk jelképezte a sámán vezető szellemét, aki megvédi őt az utazás során és segíti a baj
        okának felderítésében, és így azt gyógyíthatóvá teszi. Más kultúrákban a maszkok a természet és az élet erőit
        reprezentálják, a mítoszok ezen erőknek és energiáknak gyakran emberi vagy állati formát tulajdonítanak. Az
        élet megtartását és folytatását szolgáló ún. termékenységi rítusok során, melyek az időszakos ültetési és
        betakarítási ceremóniákkal jártak együtt, szintén fontos kellékek voltak a maszkok.</p>
    <p>A maszk egyik nagyon fontos aspektusa, hogy nem csak megváltoztatja, hanem átalakítja viselője személyiségét.
        Új-Guineában, Nyugat és Közép-Afrikában és Észak-Amerikában maszkokat az "átmeneti rítusok" során használnak.
        Az egyén vagy a társadalom életében bekövetkezett nagyon fontos változások (beavatás a felnőttkorba vagy egy
        titkos társaságba, házasság, magasabb társadalmi rang elnyerése, temetés) alkalmával rendezett szertartásokat
        gyakran jellemzik a maszkos rituálék. A születés és a halál jellemző motívumai ezeknek a szertartásoknak,
        amelyek gyakran a maszk milyenségében vizualizálódnak. Az "átmeneti rítusokban" a személyek korábbi identitása
        megszűnik és szimbolikusan egy teljesen újjal helyettesítődik.</p>
    <p>Az indián törzsek hite szerint az állatok és a szellemek az emberek "maszkjai" és ezért az emberiség
        személyesen és direkt módon felelős a kozmikus rendért. Az emberek az által fogadják el ezt a
        felelősséget, hogy a maszk segítségével állattá vagy szellemmé alakulnak, és eltáncolják, elvégzik a
        maszk szelleméhez kapcsolódó rituálékat.</p>
    <p>Az Észak-nyugati partvidék indián törzsei, nagyon sok más embercsoporthoz hasonlóan úgy hiszik, hogy a
        természetfeletti erő magában a maszkban van. Ez az erő akkor szabadul fel, ha az ember felhelyezi arcára a
        maszkot, és attól kezdve nem lesz már ugyanaz az ember: átalakul a maszk szellemévé. Valamilyen emberen túli
        természettel ruházódik fel, és a realitás egy másik, olyan dimenziójába kerül, ahol a konfliktusok és
        ellentmondások feloldódnak az univerzum alap-egységében. A rituális maszkos tánc szimbolikájának megfelelően az
        egyén, a társadalom és az univerzum ismét egységessé válik.</p>
